import UIKit

extension UIColor {
    
    open class var emerald: UIColor {
        get {
            return UIColor(colorLiteralRed: 46/255.0, green: 204/255.0, blue: 113/255.0, alpha: 1.0)
        }
    }
    
    open class var flatBlue: UIColor {
        get {
            return UIColor(colorLiteralRed: 52/255.0, green: 152/255.0, blue: 219/255.0, alpha: 1.0)
        }
    }
}
